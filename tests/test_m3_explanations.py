class TestPersistRoundTrip:

    def _make_explanation(self) -> Explanation:
        return Explanation(
            explanation_id=uuid4(),
            calc_run_id=uuid4(),
            target_node_id=uuid4(),
            target_type="Shortage",
            root_cause_node_id=uuid4(),
            causal_path=[
                CausalStep(step=1, node_id=uuid4(), node_type="CustomerOrderDemand",
                           edge_type="consumes", fact="Order requires 100 units"),
                CausalStep(step=2, node_id=uuid4(), node_type="PurchaseOrderSupply",
                           edge_type="replenishes", fact="PO provides 50 units"),
                CausalStep(step=3, node_id=None, node_type="PolicyCheck",
                           edge_type="governed_by", fact="No substitute active"),
            ],
            summary="Order requires 100 units. No substitute active.",
            created_at=datetime.now(timezone.utc),
        )

    def test_persist_calls_db_correctly(self):
        """persist() issues the correct INSERT statements."""
        explanation = self._make_explanation()
        db = MagicMock()

        builder = ExplanationBuilder()
        builder.persist(explanation, db)

        # First call: INSERT INTO explanations
        calls = db.execute.call_args_list
        assert len(calls) == 1 + len(explanation.causal_path)  # 1 header + N steps

        first_call_sql = calls[0][0][0]
        assert "INSERT INTO explanations" in first_call_sql

        # Subsequent calls: INSERT INTO causal_steps
        for i in range(1, len(calls)):
            sql = calls[i][0][0]
            assert "INSERT INTO causal_steps" in sql

    def test_get_explanation_returns_none_when_not_found(self):
        """get_explanation returns None when no row exists."""
        db = MagicMock()
        db.execute.return_value.fetchone.return_value = None

        builder = ExplanationBuilder()
        result = builder.get_explanation(uuid4(), db)

        assert result is None

    def test_get_explanation_reconstructs_from_db(self):
        """get_explanation correctly maps DB rows back to Explanation."""
        explanation_id = uuid4()
        calc_run_id = uuid4()
        target_node_id = uuid4()
        root_cause_id = uuid4()
        step_node_id = uuid4()
        created_at = datetime.now(timezone.utc)

        header_row = {
            "explanation_id": explanation_id,
            "calc_run_id": calc_run_id,
            "target_node_id": target_node_id,
            "target_type": "Shortage",
            "root_cause_node_id": root_cause_id,
            "summary": "Test summary.",
            "created_at": created_at,
        }
        step_rows = [
            {
                "step": 1,
                "node_id": step_node_id,
                "node_type": "CustomerOrderDemand",
                "edge_type": "consumes",
                "fact": "Order requires 50 units",
            },
        ]

        db = MagicMock()
        # First call fetches header row, second fetches steps
        db.execute.return_value.fetchone.return_value = header_row
        db.execute.return_value.fetchall.return_value = step_rows

        builder = ExplanationBuilder()
        result = builder.get_explanation(target_node_id, db)

        assert result is not None
        assert result.explanation_id == explanation_id
        assert result.calc_run_id == calc_run_id
        assert result.target_node_id == target_node_id
        assert result.target_type == "Shortage"
        assert result.root_cause_node_id == root_cause_id
        assert result.summary == "Test summary."
        assert len(result.causal_path) == 1
        assert result.causal_path[0].step == 1
        assert result.causal_path[0].node_id == step_node_id
        assert result.causal_path[0].node_type == "CustomerOrderDemand"
        assert result.causal_path[0].fact == "Order requires 50 units"


