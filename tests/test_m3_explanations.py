def make_node(
    node_type: str = "ProjectedInventory",
    quantity: Optional[Decimal] = None,
    has_shortage: bool = False,
    shortage_qty: Decimal = Decimal("0"),
    time_ref: Optional[date] = None,
    time_span_start: Optional[date] = None,
    time_span_end: Optional[date] = None,
    node_id: Optional[UUID] = None,
    scenario_id: Optional[UUID] = None,
) -> Node:
    return Node(
        node_id=node_id or uuid4(),
        node_type=node_type,
        scenario_id=scenario_id or uuid4(),
        quantity=quantity,
        has_shortage=has_shortage,
        shortage_qty=shortage_qty,
        time_ref=time_ref,
        time_span_start=time_span_start,
        time_span_end=time_span_end,
    )


def make_edge(
    from_node_id: UUID,
    to_node_id: UUID,
    edge_type: str,
    scenario_id: UUID,
    priority: int = 0,
) -> Edge:
    return Edge(
        edge_id=uuid4(),
        edge_type=edge_type,
        from_node_id=from_node_id,
        to_node_id=to_node_id,
        scenario_id=scenario_id,
        priority=priority,
    )


